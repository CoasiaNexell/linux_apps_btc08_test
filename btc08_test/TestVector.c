/*
 *
 *	GetGoldenParameter
 *	Parameter : midstate
 *  
 * 
 * 
 */

void GetGoldenParameter( uint8_t midstate[32], uint8_t param[12], uint8_t target[6] )
{
	memcpy(midstate, default_golden_midstate, 32);
	memcpy(param, default_golden_data, 12);
	memcpy(target, default_golden_target, 6);
}

